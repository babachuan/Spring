package cn.org.geneplus.pojo;

import com.opencsv.bean.CsvBindByName;

/**
 * @Author:quhaichuan
 * @Date:2025/6/12 11:26
 */
public class User {
    private int id;

    // 绑定csv列明
    @CsvBindByName
    private String name;

    @CsvBindByName
    private int age;

    public User() {
    }

    public User(int id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public User(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "User{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
